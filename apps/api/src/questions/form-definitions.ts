/**
 * 在维度满分内把 n 道题均分，保证之和精确等于 max（余数累加到首题，保留两位）。
 */
export function distributeScores(max: number, n: number): number[] {
  if (n <= 0) return [];
  const base = Math.floor((max / n) * 100) / 100;
  const scores = Array.from({ length: n }, () => base);
  const used = base * n;
  const remainder = Math.round((max - used) * 100) / 100;
  scores[0] = Math.round((scores[0] + remainder) * 100) / 100;
  return scores;
}
